import math  # Imports Math Module >> Use math.(any_function_present_in_that_module)
title = '=================== Math Functions in Python ==================='

note = 'Note :: Please refer code for better understanding. ' \
       'Please refer to this link for more info :: https://docs.python.org/3/library/math.html'

